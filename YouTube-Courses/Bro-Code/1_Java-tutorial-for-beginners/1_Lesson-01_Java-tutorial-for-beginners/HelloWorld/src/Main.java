public class Main {

	public static void main(String[] args) {
		
		System.out.println("Hello, Mr Tanaka!");
		System.out.println("Welcome back to the Java world!");
		
		//escape character (break lines)
		System.out.print("It's been a while since you've programmed in Java.\n");
		System.out.print("It's so good to have you back here!\n");
		
		System.out.println("We have a message to you:\n");
		System.out.println("WELCOME HOME!");
		
		//escape character (tabs)
		System.out.println("\tWELCOME!");
		
		//escape character (double quotes)
		System.out.println("\"WELCOME!\"");
		
		//escape character (backslash)
		System.out.println("\\Welcome!!!");
		
		// sysout + Ctrl + Space (shortcut)
		// System.out.println();

	}

}