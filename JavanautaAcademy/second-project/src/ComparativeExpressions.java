public class ComparativeExpressions {
    public static void main(String[] args) {
        int tapioquinha = 10;
        int coffee = 20;

        System.out.println(tapioquinha == coffee);
        System.out.println(tapioquinha != coffee);
        System.out.println(tapioquinha > coffee);
        System.out.println(tapioquinha < coffee);
        System.out.println(tapioquinha >= coffee);
        System.out.println(tapioquinha <= coffee);
    }
}